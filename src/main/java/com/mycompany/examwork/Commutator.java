public class Commutator {
    public int portOffset;
    public int num;

        public Commutator(int num) {
            this.portOffset = portOffset;
            this.num = num;
        }

        public int getPortOffset() {
            return portOffset;
        }

        @Override
        public String toString() {
            return "Коммутатор  " + num;
        }
}
